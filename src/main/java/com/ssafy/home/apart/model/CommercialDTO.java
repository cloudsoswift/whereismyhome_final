package com.ssafy.home.apart.model;

public class CommercialDTO {
	String commercial_no;
	String name;
	String branch;
	String roomNo;
	String dongNo;
	String floorNo;
	String info;
	String address;
	String lat;
	String lng;
	public String getCommercial_no() {
		return commercial_no;
	}
	public void setCommercial_no(String commercial_no) {
		this.commercial_no = commercial_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	public String getDongNo() {
		return dongNo;
	}
	public void setDongNo(String dongNo) {
		this.dongNo = dongNo;
	}
	public String getFloorNo() {
		return floorNo;
	}
	public void setFloorNo(String floorNo) {
		this.floorNo = floorNo;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	
}
